package com.kyletung.doubanbookmovie.movie;

public class MovieItemData {

    private String movieId;
    private String movieName;
    private String movieImage;
    private int moviePoints;
    private String movieDirector;
    private String movieCasts;
    private String movieYear;
    private int movieCollections;

    public String getMovieId() {
        return movieId;
    }

    public void setMovieId(String movieId) {
        this.movieId = movieId;
    }

    public String getMovieName() {
        return movieName;
    }

    public void setMovieName(String movieName) {
        this.movieName = movieName;
    }

    public String getMovieImage() {
        return movieImage;
    }

    public void setMovieImage(String movieImage) {
        this.movieImage = movieImage;
    }

    public int getMoviePoints() {
        return moviePoints;
    }

    public void setMoviePoints(int moviePoints) {
        this.moviePoints = moviePoints;
    }

    public String getMovieDirector() {
        return movieDirector;
    }

    public void setMovieDirector(String movieDirector) {
        this.movieDirector = movieDirector;
    }

    public String getMovieCasts() {
        return movieCasts;
    }

    public void setMovieCasts(String movieCasts) {
        this.movieCasts = movieCasts;
    }

    public String getMovieYear() {
        return movieYear;
    }

    public void setMovieYear(String movieYear) {
        this.movieYear = movieYear;
    }

    public int getMovieCollections() {
        return movieCollections;
    }

    public void setMovieCollections(int movieCollections) {
        this.movieCollections = movieCollections;
    }
}
