package com.kyletung.doubanbookmovie.home;

public class HomeItemData {

    private String homeId;
    private String homeName;
    private String homeImage;
    private int homePoints;
    private String homeDirector;
    private String homeCasts;
    private String homeYear;
    private int homeCollections;

    public String getHomeId() {
        return homeId;
    }

    public void setHomeId(String homeId) {
        this.homeId = homeId;
    }

    public String getHomeName() {
        return homeName;
    }

    public void setHomeName(String homeName) {
        this.homeName = homeName;
    }

    public String getHomeImage() {
        return homeImage;
    }

    public void setHomeImage(String homeImage) {
        this.homeImage = homeImage;
    }

    public int getHomePoints() {
        return homePoints;
    }

    public void setHomePoints(int homePoints) {
        this.homePoints = homePoints;
    }

    public String getHomeDirector() {
        return homeDirector;
    }

    public void setHomeDirector(String homeDirector) {
        this.homeDirector = homeDirector;
    }

    public String getHomeCasts() {
        return homeCasts;
    }

    public void setHomeCasts(String homeCasts) {
        this.homeCasts = homeCasts;
    }

    public String getHomeYear() {
        return homeYear;
    }

    public void setHomeYear(String homeYear) {
        this.homeYear = homeYear;
    }

    public int getHomeCollections() {
        return homeCollections;
    }

    public void setHomeCollections(int homeCollections) {
        this.homeCollections = homeCollections;
    }
}
